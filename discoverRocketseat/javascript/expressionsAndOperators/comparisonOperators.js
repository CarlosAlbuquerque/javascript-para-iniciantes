/* Comparison operators
Irá comparar e retornar um valor Boolen como resposta a comparação */

let one = 1
let two = 2

// == igual
console.log(one == 1)
// != diferente
console.log(one != two)
// === estritamente igual
console.log(one === "1")
console.log(one === 1) 
// !== estritamente diferente
console.log(one !== "1")

// > Maior
console.log(one > two)
// >= Maior ou Igual
console.log(two >= 2)
// < Menor
console.log(one < two)
// <= Menor ou Igual
console.log(one <= 1)
