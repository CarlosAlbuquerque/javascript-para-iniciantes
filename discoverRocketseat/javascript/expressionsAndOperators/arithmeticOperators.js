// Arithmetic operators


const number1 = 20
const number2 = 10

// Multiplicação
console.log(number1 * number2)
// Divisão
console.log(number1 / number2)
// Soma
console.log(number1 + number2)
// Subtração
console.log(number1 - number2)
// Resto da Divisão
console.log(number1 % number2)
// Incremento
let increment = 1
