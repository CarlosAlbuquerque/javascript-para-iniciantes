
//WHILE
var c = 1

while (c<=5) {
    console.log(`C tem o valor de ${c} usando WHILE!`)    
    c++
}

//DO WHILE
var d = 1
do{
    console.log(`D tem o valor de ${d} usando DO WHILE!`)    
    d++
} while (d <= 5) 


//FOR
for (let c = 1; c <= 5 ; c++) {
    console.log(`C tem o valor de ${c} usando FOR!`)    
}